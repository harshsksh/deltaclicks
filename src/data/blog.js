export function getPostBySlug(slug) {
  return blog.find(post => post.slug === slug);
}

export function getFeaturedPost() {
  return blog.find(post => post.featured);
}

export function getPostsByCategory(category) {
  if (category === "All") return blog;
  return blog.filter(post => post.category === category);
}

export function getBlogCategories() {
  const categories = blog.map(post => post.category);
  return ["All", ...new Set(categories)];
}

export function getRelatedPosts(category, currentSlug, limit = 3) {
  return blog
    .filter(post => post.category === category && post.slug !== currentSlug)
    .slice(0, limit);
}
